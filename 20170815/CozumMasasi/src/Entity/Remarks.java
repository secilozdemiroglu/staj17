package Entity;

public class Remarks {

	int RemarkID;
	String remark;
	int ProblemID;

	public int getRemarkID() {
		return RemarkID;
	}

	public void setRemarkID(int remarkID) {
		RemarkID = remarkID;
	}

	public int getProblemID() {
		return ProblemID;
	}

	public void setProblemID(int problemID) {
		ProblemID = problemID;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
